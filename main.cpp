#include <iostream>
using namespace std;

void main()
{
	int intX = 3, intB = 5;
	float floatX = 4.0f, floatY = 7.0f;

	cout << intX + intB << "\n";
	cout << intX - intB << "\n";
	cout << intX * intB << "\n";
	cout << intX / intB << "\n";
	cout << "\n";
	cout << floatX + floatY << "\n";
	cout << floatX - floatY << "\n";
	cout << floatX * floatY << "\n";
	cout << floatX / floatY << "\n";
}